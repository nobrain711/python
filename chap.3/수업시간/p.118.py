# 어떤 수가 짝수인지 홀수인지 판별하는 프로그램

# 결과를 바로 출력

x = int(input("숫자를 입력하세요.:"))

if x % 2 == 0 :
    print('짝수 입니다.')
    print('2로 나눈 나머지가 0이다')
else :
    print('홀수 입니다.')
    print('2로 나눈 나머지가 0아니다')

